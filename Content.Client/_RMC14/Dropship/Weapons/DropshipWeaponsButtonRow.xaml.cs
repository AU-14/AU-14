using Content.Client.Message;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client._RMC14.Dropship.Weapons;

[GenerateTypedNameReferences]
public sealed partial class DropshipWeaponsButtonRow : BoxContainer
{
    public DropshipWeaponsButtonRow()
    {
        RobustXamlLoader.Load(this);
    }

    public void Refresh()
    {
        switch (Orientation)
        {
            case LayoutOrientation.Horizontal:
                PaddingStart.HorizontalExpand = true;
                PaddingEnd.HorizontalExpand = true;

                PaddingStart.VerticalExpand = false;
                PaddingEnd.VerticalExpand = false;
                break;
            case LayoutOrientation.Vertical:
                PaddingStart.HorizontalExpand = false;
                PaddingEnd.HorizontalExpand = false;

                PaddingStart.VerticalExpand = true;
                PaddingEnd.VerticalExpand = true;
                break;
        }
    }

    public void SetNames(
        string? one = null,
        string? two = null,
        string? three = null,
        string? four = null,
        string? five = null)
    {
        static string Wrap(string? text)
        {
            return $"[font size=8][color=white]{text ?? string.Empty}[/color][/font]";
        }

        One.RichLabel.HorizontalAlignment = HAlignment.Center;
        One.RichLabel.SetMarkupPermissive(Wrap(one));
        if (one == null)
            One.SetOnPressed(null);

        Two.RichLabel.HorizontalAlignment = HAlignment.Center;
        Two.RichLabel.SetMarkupPermissive(Wrap(two));
        if (two == null)
            Two.SetOnPressed(null);

        Three.RichLabel.HorizontalAlignment = HAlignment.Center;
        Three.RichLabel.SetMarkupPermissive(Wrap(three));
        if (three == null)
            Three.SetOnPressed(null);

        Four.RichLabel.HorizontalAlignment = HAlignment.Center;
        Four.RichLabel.SetMarkupPermissive(Wrap(four));
        if (four == null)
            Four.SetOnPressed(null);

        Five.RichLabel.HorizontalAlignment = HAlignment.Center;
        Five.RichLabel.SetMarkupPermissive(Wrap(five));
        if (five == null)
            Five.SetOnPressed(null);
    }
}
